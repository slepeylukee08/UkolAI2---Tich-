# Vytvořte pole s 5 řetězcemi
seznam = ["jablko", "banán", "pomeranč", "hruška", "jahoda"]
print(seznam)

# Přidání nového prvku "vítr" na konec seznamu
seznam.append("vítr")
print(seznam)

# Odstranění druhého prvku (s hodnotou "banán")
seznam.remove("banán")
print(seznam)

# Zaměnění pátého prvku za "slunce"
seznam[4] = "slunce"
print(seznam)

# Přidání dvou nových hodnot "měsíc" a "hvězdy" pomocí metody extend()¨
seznam.extend(["měsíc", "hvězdy"])
print(seznam)

# Základní pole
